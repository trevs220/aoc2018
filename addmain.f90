PROGRAM ADDMAIN
  IMPLICIT NONE
  DOUBLE PRECISION :: CURRDISP, NEWDISP
  LOGICAL :: FILEEXIST
  INTEGER :: I,M,IO
  INQUIRE(FILE='input.dat', EXIST=FILEEXIST)
  IF (FILEEXIST .eqv. .TRUE.) THEN
    OPEN(UNIT=24, FILE='input.dat')
  ELSE
    WRITE(*,*) 'NO INPUT FILE'
  END IF
  CURRDISP = 0;
  DO
    READ(UNIT=24,FMT=*,IOSTAT=IO) NEWDISP
    IF (IO/=0) EXIT
    CURRDISP = CURRDISP + NEWDISP
  END DO
  WRITE(*,*) CURRDISP


END PROGRAM
